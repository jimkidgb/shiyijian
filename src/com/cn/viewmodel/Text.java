package com.cn.viewmodel;

public class Text {
	private String id;
	private String text;
	private String pid;
	public String getId() {
		return id;
	}
	public String getText() {
		return text;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
}
