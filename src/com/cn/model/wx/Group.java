package com.cn.model.wx;

public class Group {
	private String id;
	private String text;
	private String rtid;
	public String getId() {
		return id;
	}
	public String getText() {
		return text;
	}
	public String getRtid() {
		return rtid;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setText(String text) {
		this.text = text;
	}
	public void setRtid(String rtid) {
		this.rtid = rtid;
	}
}
